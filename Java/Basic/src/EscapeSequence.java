
public class EscapeSequence {

	public static void main(String[] args) {
		System.out.println("Hello World!!");
		System.out.print("Hello World!!");
		System.out.println("Hello World!!");
		System.out.println("Hello\tWorld!!");
		System.out.println("Hello World!!");
		System.out.println("d/s");

	}

}
